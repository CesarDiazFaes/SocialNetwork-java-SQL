package datos;

public class Actor {
	
	private String nombre;
	private String personaje;
	
	public Actor(String nombre, String personaje) {
		this.nombre = nombre;
		this.personaje = personaje;
	}
	
	public String getNombre() {
		return nombre;
	}
	public String getPersonaje() {
		return personaje;
	}
	
	
}
